const codons = {
  "AUG": "Methionine",
  "UUU": "Phenylalanine",
  "UUC": "Phenylalanine",
  "UUA": "Leucine",
  "UUG": "Leucine",
  "UCU": "Serine",
  "UCC": "Serine",
  "UCA": "Serine",
  "UCG": "Serine",
  "UAU": "Tyrosine",
  "UAC": "Tyrosine",
  "UGU": "Cysteine",
  "UGC": "Cysteine",
  "UGG": "Tryptophan",
  "UAA": "STOP",
  "UAG": "STOP",
  "UGA": "STOP",
};

export const translate = (RNA = '') => {
  const protein = codons[RNA.slice(0,3)];
  if (!RNA.length | protein === 'STOP')
  return [];

  if (!protein) throw new Error('Invalid codon');
  return [protein, ...translate(RNA.slice(3))];
};